/* Rita ett tecken på skärmen med VGA-färg (index 0-15) */
void draw_char(int x, int y, char c, uint8_t color) {
    uint8_t* glyph;
    unsigned char uc = (unsigned char)c;
    
    /* Hantera svenska tecken (värden 128-143) */
    if(uc >= 128 && uc <= 143) {
        glyph = font_8x8[uc];
    }
    /* Standard ASCII 32-126 */
    else if(uc >= 32 && uc <= 126) {
        glyph = font_8x8[uc - 32];
    }
    else {
        glyph = font_8x8[0]; /* Mellanslag för ogiltiga tecken */
    }
    
    for(int row = 0; row < 8; row++) {
        for(int col = 0; col < 8; col++) {
            if(glyph[row] & (1 << (7 - col))) {
                vga_put_pixel(x + col, y + row, color);  /* Ändrat från fb_put_pixel */
            }
        }
    }
}

/* Rita en sträng på skärmen */
void draw_string(int x, int y, const char* str, uint8_t color) {
    int start_x = x;
    while(*str) {
        if(*str == '\n') {
            x = start_x;
            y += 8; /* Radavstånd */
        } else {
            draw_char(x, y, *str, color);
            x += 8; /* Teckenbredd */
        }
        str++;
    }
}

/* Rita en siffra (för enkel användning) */
void draw_number(int x, int y, uint32_t num, uint8_t color) {
    char buffer[16];
    int i = 0;
    
    if(num == 0) {
        draw_char(x, y, '0', color);
        return;
    }
    
    while(num > 0) {
        buffer[i++] = '0' + (num % 10);
        num /= 10;
    }
    
    for(int j = i - 1; j >= 0; j--) {
        draw_char(x, y, buffer[j], color);
        x += 8;
    }
}

/* Rita en hexadecimal siffra */
void draw_hex(int x, int y, uint32_t num, uint8_t color) {
    const char* hex_digits = "0123456789ABCDEF";
    
    draw_char(x, y, '0', color);
    draw_char(x + 8, y, 'x', color);
    
    for(int i = 0; i < 8; i++) {
        int shift = 28 - (i * 4);
        uint8_t digit = (num >> shift) & 0xF;
        draw_char(x + 16 + i * 8, y, hex_digits[digit], color);
    }
}